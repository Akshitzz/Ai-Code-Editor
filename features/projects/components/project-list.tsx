import { Spinner } from "@/components/ui/spinner"
import { useProjectparital } from "../hooks/use-project"
import { Kbd } from "@/components/ui/kbd"
import { Doc } from "../../../convex/_generated/dataModel"
import Link from "next/link"
import { AlertCircleIcon, ArrowRightIcon, GlobeIcon, Loader2Icon } from "lucide-react"
import { formatDistanceToNow } from "date-fns"
import { FaGithub } from "react-icons/fa"
import { Button } from "@/components/ui/button"
import { authClient } from "@/lib/auth-client"


interface ProjectListProps {
    onViewAll: () => void
}

const ContinueCard = ({ data }: { data: Doc<"projects"> }) => {
    return (
        <div className=" flex flex-col gap-2">
            <span className="text-xs text-muted-foreground">
                Continue working on ..
            </span>
            <Button
                variant="outline"
                asChild
                className="h-auto items-start justify-start p-4 bg-background borrder rounded-none flex flex-col gap-2"
            >
                <Link
                    href={`/project/${data._id}`}
                    className="group"
                >
                    <div className="flex items-center gap-2">
                        {getProjectIcon(data)}
                        <span className="font-medium truncate">
                            {data.name}
                        </span>
                    </div>
                    <ArrowRightIcon
                        className="size-4 text-muted-foreground group-hover:translate-x-0.5 transition-transform"
                    />
                    <span className="text-xs text-muted-foreground">
                        {formatTimestamp(data.updatedAt)}
                    </span>
                </Link>
            </Button>
        </div>
    )
}

const formatTimestamp = (timestamp: number) => {
    return formatDistanceToNow(timestamp, {
        addSuffix: true,
    })
}

const getProjectIcon = (project: Doc<"projects">) => {
    if (project.importStatus === "completed") {
        return <FaGithub className="size-4 text-foreground" />
    }
    if (project.importStatus === "failed") {
        return <AlertCircleIcon className="size-4 text-foreground" />
    }
    if (project.importStatus === "importing") {
        return <Loader2Icon className="size-4 text-foreground animate-spin" />
    }
    return <GlobeIcon className="size-4 text-foreground" />
}

const ProjectItem = ({ data }: {
    data: Doc<"projects">
}) => {
    return (
        <Link
            href={`/project/${data._id}`}
            className="text-sm text-foreground/60 font-medium hover:text-foreground py-1 flex items-center justify-between w-full group"
        >
            <div className="flex item-center gap-2 ">
                {getProjectIcon(data)}
                <span className="truncate">{data.name}</span>
            </div>
            <span className="text-xs text-muted-foreground group-hover:text-foreground/60 transition-colors">
                {formatTimestamp(data.updatedAt)}
            </span>
        </Link>
    )
}


export const ProjectList = ({ onViewAll }: ProjectListProps) => {
    const { data: session, isPending: sessionLoading } = authClient.useSession()
    const projects = useProjectparital(session ? 6 : "skip")

    if (sessionLoading) {
        return <Spinner className="size-4 text-ring" />
    }

    if (!session) {
        return null;
    }

    if (projects === undefined) {
        return <Spinner className="size-4 text-ring" />
    }

    const [mostRecent, ...rest] = projects;

    return (
        <div className="flex flex-col gap-4">
            {mostRecent ?
                <ContinueCard data={mostRecent} /> : null
            }
            {rest.length > 0 && (
                <div className="flex flex-col gap-2">
                    <div className="flex items-center justify-between gap-2">
                        <span className="text-xs text-muted-foreground">
                            Recent Projects
                        </span>
                        <button
                            onClick={onViewAll}
                            className="flex items-center gap-2 text-muted-foreground transition-colors hover:text-foreground text-xs">
                            <span>View All</span>
                            <Kbd className="bg-accent border">
                                Ctrl+K
                            </Kbd>
                        </button>
                    </div>
                    <ul className="flex flex-col gap-2">
                        {rest.map((project) => (
                            <ProjectItem
                                key={project._id}
                                data={project} />
                        ))}
                    </ul>
                </div>
            )}
        </div>
    )
}

